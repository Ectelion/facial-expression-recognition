normalizeMatrix <- function(matrix) {
	col1   <- matrix[, 1]
	col2   <- matrix[, 2]
	matrix[, 1] <- normalizeVector(col1)
	matrix[, 2] <- normalizeVector(col2)
	matrix
}

normalizeVector <- function(vector) {
	s <- sqrt(var(vector))
	normalizedVector <- (vector-mean(vector)) / s
	normalizedVector
}

